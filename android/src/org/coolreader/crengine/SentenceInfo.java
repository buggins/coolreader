package org.coolreader.crengine;

public class SentenceInfo {
	public int startX;
	public int startY;
	public String text;

	public SentenceInfo() {
	}

	public void setStartX(int startX){
		this.startX = startX;
	}
	public void setStartY(int startY){
		this.startY = startY;
	}
	public void setText(String text){
		this.text = text;
	}
}
